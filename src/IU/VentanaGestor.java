     @Override
    public void mostrarPedidosPendientes(List<Pedido> pendientes) {
        DefaultListModel<String> lista = new DefaultListModel<>();
        for(Pedido p : pendientes){
            lista.addElement(
                p.getItem().getNombre() +"- cliente: " + p.getNombreCliente()
                );
            }
            listPedidosPendientes.setModel(lista);
        }

        @Override
        public void mostrarPedidosTomados(List<Pedido> tomados) {
            String[] columnas = { "Nombre de √çtem", "Comentario", "Cliente", "Fecha/Hora", "Estado" };
            Object[][] datos = new Object[tomados.size()][columnas.length];
            for (int i = 0; i < tomados.size(); i++) {
                Pedido p = tomados.get(i);
                datos[i][0] = p.getItem().getNombre();
                datos[i][1] = p.getComentario();
                datos[i][2] = p.getNombreCliente();
                datos[i][3] = p.getFechaCreacion();
                datos[i][4] = p.getEstado();
            }
            tblPedidosTomados.setModel(new javax.swing.table.DefaultTableModel(datos, columnas));
        }

        @Override
        public void mostrarError(String mensaje) {
            lblMensajes.setText("<html>" + mensaje + "</html>");
        }
        
